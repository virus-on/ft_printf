#include "libft.h"

int		ft_wordcount(char const *s, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if ((s[i - 1] == '\0' || s[i - 1] == c) && (s[i] != c))
			j++;
		i++;
	}
	return (j);
}
